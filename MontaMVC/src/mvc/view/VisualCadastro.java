    public void cadastro(){
        
        btnNovo.setEnabled(true);
        
        Loja lj = new Loja();
        LojaControl lc = new LojaControl();
        
        lj.setNomeproduto(txtNome.getText());
        lj.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        lj.setPeso(Double.parseDouble(txtPeso.getText()));
        lj.setPreco(Double.parseDouble(txtPreco.getText()));
        
        
        lc.inserir(lj.getNomeproduto(), lj.getQuantidade(), lj.getPeso(), lj.getPreco());
        
        novo();
        
        txtId.setEnabled(true);
        
        btnNovo.setEnabled(true);
        
    }
    
    public void apagar(){
        
        Loja lj = new Loja();
        LojaControl lc = new LojaControl();
        
        lj.setIdproduto(Integer.parseInt(txtId.getText()));
        
        lc.apagar(lj.getIdproduto());            
    }
    
    public void novo(){
        
        btnCadastro.setEnabled(true);
        
        txtId.setText("");
        txtNome.setText("");
        txtQuantidade.setText("");
        txtPeso.setText("");
        txtPreco.setText("");       
        
        btnNovo.setEnabled(false);
        
    }
    
    
    
